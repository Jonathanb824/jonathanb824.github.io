// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(number) {
  // create empty string
  let line = "";
  // loop through number
  for (var y = 0; y < number; y++){
    //loop through it again
    for (var x = 0; x < number; x++){
      if((x + y) % 2 === 0){
        line += ' ';
      } else {
        line += '#'
    }
  }
line += '\n';
}
console.log(line);

}

